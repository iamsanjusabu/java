package com.sanjusabu.javacompletecourse;

public class DataTypes4 {
    public static void main(String[] args) {
        // PRIMITIVE DATATYPES
        // Whole numbers
        byte num1 = 10;
        int num2 = 20; // Preffered (1)
        short num3 = 30;
        long num4 = 40L; // Preffered (2)

        // Decimal numbers
        float num5 = 100.5F;
        double num6 = 3.14159;

        // Chararacter
        char gender = 'M';

        // Boolean 
        boolean isMale = true;
        boolean isFemale = false;

        // REFERENCE DATATYPES
        String name = "Sanju Sabu";

        System.out.println("Name: " + name);
    }
}